public class Data01 {
    public static void main(String[] args){

        // (1) 정수 : 소수점이 없는 수
        System.out.println(1);
        // (2) 실수 : 소수점이 있는 수
        System.out.println(1.5);
        // (3) 문자 : 단일 따옴표로 묶어줌
        System.out.println('a');
        // (4) 논리값 : true, false
        System.out.println(true);
    }
}
