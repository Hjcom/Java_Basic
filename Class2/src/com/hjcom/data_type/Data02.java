package com.hjcom.data_type;

// 실수형 상수 예제
public class Data02 {
    public static void main(String[] args){
        // (1) long 형 상수 : 숫자 끝에 L 혹은 l을 붙임
        System.out.println(1L);
        // (2) float 형 상수 : 숫자 끝에 F 혹은 f를 붙임
        System.out.println(1.5f);
        // (3) 문자열 : 이중 따옴표로 묶어줌
        System.out.println("abc");
    }
}
